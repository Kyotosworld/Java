class Point {

    //private double[] coord;
    private double x;
    private double y;
    //private double z;


   /***************************************************************************
    * Point                                                                   *
    ***************************************************************************
    * @param x                                                                *
    * @param y                                                                *
    ***************************************************************************/
    Point(double x, double y) {
        this.x = x;
        this.y = y;
    } /* FIN Point */


    /****************************
     *                          *
     *        Accesseurs        *
     *                          *
     ****************************/
    double getX() {
        return this.x;
    }
    double getY() {
        return this.y;
    }
}
